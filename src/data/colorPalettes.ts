export function getPaletteBySlug(slug: string): ColorPalettePreset | undefined {
  return colorPalettes.find(p => p.slug === slug)
}

export function getRelatedPalettes(slug: string): ColorPalettePreset[] {
  const palette = getPaletteBySlug(slug)
  if (!palette) return []
  return palette.relatedPalettes
    .map(s => getPaletteBySlug(s))
    .filter((p): p is ColorPalettePreset => p !== undefined)
}

export function getPalettesByCategory(category: string): ColorPalettePreset[] {
  return colorPalettes.filter(p => p.category === category)
}
