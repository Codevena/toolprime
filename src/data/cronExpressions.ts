export function getCronBySlug(slug: string): CronExpression | undefined {
  return cronExpressions.find((c) => c.slug === slug)
}

export function getRelatedCronExpressions(slug: string): CronExpression[] {
  const expr = getCronBySlug(slug)
  if (!expr) return []
  return expr.relatedExpressions
    .map((s) => getCronBySlug(s))
    .filter((e): e is CronExpression => e !== undefined)
}
